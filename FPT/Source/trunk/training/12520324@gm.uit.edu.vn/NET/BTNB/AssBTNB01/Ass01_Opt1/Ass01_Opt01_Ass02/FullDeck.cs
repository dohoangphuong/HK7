using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Ass01_Opt01_Ass02
{
    class FullDeck
    {
        public void ShowAllCard()
        {
            Card cd = new Card();
            for (int i = 0; i < 13; i++)
            {
                for (int j = 0; j < 4; j++)
                {
                    Console.WriteLine(cd.getRanks(i + 1) + " - " + cd.getSuits(j + 1));
                }
            }
        }
    }
}
