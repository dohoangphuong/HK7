using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Assignment3
{
    class Faculty
    {
        /// <summary>
        /// Faculty Id
        /// </summary>
        private int intFacultyId;

        /// <summary>
        /// Faculty Name
        /// </summary>
        private string strFacultyName;
        public string FacultyName
        {
            get { return strFacultyName; }
            set { strFacultyName = value; }
        }

        /// <summary>
        /// the constructor of class
        /// </summary>
        /// <param name="id"></param>
        /// <param name="facultyName"></param>
        public Faculty(int id, string facultyName)
        {
            intFacultyId = id;
            strFacultyName = facultyName;
        }
    }
}
